    /**
     * Preencher combobox estados
     */
    private void listarEstados() {
        listaModelEstados = controllerEstado.getListaEstadoController();
        cbEstado.removeAllItems();
        for (int i = 0; i < listaModelEstados.size(); i++) {
            cbEstado.addItem(listaModelEstados.get(i).getUf());
        }
    }

    /**
     * Preencher combobox cidades
     */
    private void listarCidades() {
        listaModelCidades = controllerCidade.getListaCidadePorEstadoController(controllerEstado.getEstadoUFController(this.cbEstado.getSelectedItem().toString()).getCodigo());
        cbCidade.removeAllItems();
        for (int i = 0; i < listaModelCidades.size(); i++) {
            cbCidade.addItem(listaModelCidades.get(i).getNome());
        }
    }

    private boolean alteraFornecedor() {
        modelFornecedor.setCodigo(Integer.parseInt(this.tfCodigo.getText()));
        modelFornecedor.setNome(this.tfNome.getText());
        modelFornecedor.setEndereco(this.tfEndereco.getText());
        modelFornecedor.setBairro(this.tfBairro.getText());
        modelFornecedor.setCNPJ(this.tfCNPJ.getText());
        modelFornecedor.setIE(this.tfIE.getText());
        modelFornecedor.setEmail(this.tfEmail.getText());
        modelFornecedor.setCodCidade(controllerCidade.getCidadeController(this.cbCidade.getSelectedItem().toString()).getCodigo());
        modelFornecedor.setCodEstado(controllerEstado.getEstadoUFController(this.cbEstado.getSelectedItem().toString()).getCodigo());
        modelFornecedor.setCep(this.tfCep.getText());
        modelFornecedor.setTelefone(this.tfTelefone.getText());

        //alterar 
        if (controllerFornecedor.atualizarFornecedorController(modelFornecedor)) {
            JOptionPane.showMessageDialog(this, "Registro alterado com sucesso!");
            this.desabilitarCampos();
            this.carregarFornecedores();
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private boolean salvarFornecedor() {
        modelFornecedor.setNome(this.tfNome.getText());
        modelFornecedor.setEndereco(this.tfEndereco.getText());
        modelFornecedor.setBairro(this.tfBairro.getText());
        modelFornecedor.setCNPJ(this.tfCNPJ.getText());
        modelFornecedor.setIE(this.tfIE.getText());
        modelFornecedor.setEmail(this.tfEmail.getText());
        modelFornecedor.setCodCidade(controllerCidade.getCidadeController(this.cbCidade.getSelectedItem().toString()).getCodigo());
        modelFornecedor.setCodEstado(controllerEstado.getEstadoUFController(this.cbEstado.getSelectedItem().toString()).getCodigo());
        modelFornecedor.setCep(this.tfCep.getText());
        modelFornecedor.setTelefone(this.tfTelefone.getText());

        //salvar 
        if (controllerFornecedor.salvarFornecedorController(modelFornecedor) > 0) {
            JOptionPane.showMessageDialog(this, "Registro gravado com sucesso!");
            this.desabilitarCampos();
            this.carregarFornecedores();
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao gravar os dados!", "ERRO", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    private boolean recuperarFornecedor() {
        //recebe a linha selecionada
        int linha = this.tbFornecedores.getSelectedRow();

        //pega o codigo do cliente na linha selecionada
        int codigo = (Integer) tbFornecedores.getValueAt(linha, 0);

        try {
            //recupera os dados do banco
            modelFornecedor = controllerFornecedor.getFornecedorController(codigo);
            //seta os dados na interface
            this.tfCodigo.setText(String.valueOf(modelFornecedor.getCodigo()));
            this.tfNome.setText(modelFornecedor.getNome());
            this.tfEndereco.setText(modelFornecedor.getEndereco());
            this.tfBairro.setText(modelFornecedor.getBairro());
            this.tfCNPJ.setText(modelFornecedor.getCNPJ());
            this.tfIE.setText(modelFornecedor.getIE());
            this.tfEmail.setText(modelFornecedor.getEmail());
            this.cbEstado.setSelectedItem(controllerEstado.getEstadoController(modelFornecedor.getCodEstado()).getUf());
            this.listarCidades();
            this.cbCidade.setSelectedItem(controllerCidade.getCidadeController(modelFornecedor.getCodCidade()).getNome());
            System.err.println(controllerCidade.getCidadeController(modelFornecedor.getCodCidade()).getNome());
            this.tfCep.setText(modelFornecedor.getCep());
            this.tfTelefone.setText(modelFornecedor.getTelefone());
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }

    private void carregarFornecedores() {
        listaModelFornecedorCidadeEstados = controllerFornecedorCidadeEstado.getlistaFornecedorCidadeEstados();
        DefaultTableModel modelo = (DefaultTableModel) tbFornecedores.getModel();
        modelo.setNumRows(0);
        //CARREGA OS DADOS DA LISTA NA TABELA
        int cont = listaModelFornecedorCidadeEstados.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelFornecedorCidadeEstados.get(i).getModelFornecedor().getCodigo(),
                listaModelFornecedorCidadeEstados.get(i).getModelFornecedor().getNome(),
                listaModelFornecedorCidadeEstados.get(i).getModelCidade().getNome(),
                listaModelFornecedorCidadeEstados.get(i).getModelEstado().getUf()
            });
        }
    }

